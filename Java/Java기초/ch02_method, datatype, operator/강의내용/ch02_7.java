package 강의내용;
public class ch02_7 {

	public static void main(String[] args) {
		System.out.println('a' < 'b');
		System.out.println(3>=2);
		System.out.println(-1<0);
		System.out.println(3!=2);
		System.out.println(!(3!=2));
		System.out.println((3>2) && (3<2));
		System.out.println((3!=2) || (-1 > 0));
		System.out.println((3!=2) ^ (-1 > 0));
		System.out.println(2!=2);
		
	}

}
